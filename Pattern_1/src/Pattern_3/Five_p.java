package Pattern_3;

public class Five_p {
	public static void main(String[] args) {
		
	/**	A A A A A 
		B B B B B 
		C C C C C 
		D D D D D 
		E E E E E 
*/
		char i;
		char j;
		for( i='A';i<='E';i++) {
			for(j='A';j<='E';j++) {
				System.out.print(i+" ");
			}
			System.out.println();
		}
	

	}
}
